package controlStatementProgram;

public class SwapThreeno {
	public static void main(String[] args) {
		int no1=12;
		int no2=23;
		System.out.println("before swapping no1="+no1+", no2="+ no2);
		 
		int temp=no1;
		 no1=no2;
		 no2=temp;
		 System.out.println("After swapping no1="+no1+", no2="+ no2);
		 
		
	}

}
