package controlStatementProgram;

public class FactorialNo {
	public static void main(String[] args) {
		int no=3;
		if(no<0)
		{
			System.out.println("Factorial is not define for negetive no:");
		}

		else {
			
				long fact=1;
				
				for(int i=1;i<=no;i++)
				{
					fact=fact*i;
					
					
				}
				System.out.println("factorial of:"+no+"is:"+fact);
		}
	}
}

				
				
				
				
				
				
				
				
				
			
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
		