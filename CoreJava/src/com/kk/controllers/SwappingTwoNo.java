package com.kk.controllers;

public class SwappingTwoNo {
	public static void main(String[] args) {
		int no1=20;
		int no2=43;
		System.out.println("before swapping no: no1="+no1+",no2="+no2);
		int temp=no1;
		no1=no2;
		no2=temp;
		System.out.println("after swapping no: no1="+no1+",no2="+no2);
		
	}
	

}
