package com.kk.controllers;

public class ControlStatement {
	public static void main(String[] args) {
		int a = 34;
		int b = 45;
		int c = 67;
		if (a >= b && a >= c) {
			System.out.println(" a greater");

		} else if (b > c && b > c) {
			System.out.println("b greater");

		} else {
			System.out.println("c is greater");
		}

	}

}
